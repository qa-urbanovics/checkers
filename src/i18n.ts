export type TKey = keyof typeof T.en;
type Lang = 'en' | 'ru' | 'es';

export function detectLanguage(): 'en' | 'ru' | 'es' {
  const lang = (navigator.language || 'en').toLowerCase();
  if (lang.startsWith('ru')) return 'ru';
  if (lang.startsWith('es')) return 'es';
  return 'en';
}

export function useT() {
  const lang = useGameStore(s => s.settings.language) as Lang;
  const strings = T[lang] ?? T.en;
  return (key: TKey): string => strings[key] ?? T.en[key];
}
